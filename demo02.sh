#!/bin/bash

# ======================================== 数组 =======================================

# bash支持一维数组， 不支持多维数组，并且没有限定数组的大小



# ======== 定义数组 =========
arr1=(1 2 3 4 hello)

# ======== 读取数组 =========
echo ${arr1[1]} #输出 2
echo ${arr1[@]} #使用@符号输出全部元素

# ======== 获取数组长度 =========
len1=${#arr1[@]}
len2=${#arr1[*]}
len3=${#arr1[4]} #获取某个元素的长度
echo $len1 $len2 $len3